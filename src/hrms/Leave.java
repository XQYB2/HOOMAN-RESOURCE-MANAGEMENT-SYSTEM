    public void loadData(){
    
        try {
              Class.forName("com.mysql.cj.jdbc.Driver");
              
              String url = "jdbc:MySQL://localhost:3308/hrms";
              String user = "root";
              String pass = "";
              
              Connection con = DriverManager.getConnection(url,user,pass);
              Statement st = con.createStatement();
              
              DefaultTableModel model = new DefaultTableModel(
              new String[]{"LeaveID", "EmployeeName", "LeaveType", "StartDate", "EndDate", 
                  "Status"}, 0);

              
              Table1.setModel(model);
              String sql = "SELECT l.LeaveID, e.EmployeeName, l.LeaveType, l.StartDate, l.EndDate, " +
             "l.Status " +
             "FROM processleave l " +
             "INNER JOIN employeeinfo e ON l.EmployeeID = e.EmployeeID";

              
              ResultSet rs = st.executeQuery(sql);
              
              
              String sid, ename, bs,bn,bacc,pm;
              while (rs.next()){
              sid = rs.getString("LeaveID");
              ename = rs.getString("EmployeeName");
              bs = rs.getString("LeaveType");
              bn = rs.getString("StartDate");
              bacc = rs.getString("EndDate");
              pm = rs.getString("Status");
              
              
              model.addRow(new Object []{sid, ename, bs,bn,bacc,pm});
              }
        }
    catch (Exception e){
              System.out.println("Error" + e.getMessage());
          }
}

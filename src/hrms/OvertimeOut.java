         String emid, lt, query; 
      java.util.Date sd;
       try{
         Class.forName("com.mysql.cj.jdbc.Driver");  
              String url = "jdbc:MySQL://localhost:3308/hrms";
              String user = "root";
              String pass = "";
              
              Connection con = DriverManager.getConnection(url,user,pass);
              Statement st = con.createStatement();
              
              if("".equals(SearchID.getText())){
              JOptionPane.showMessageDialog(new JFrame(), "Employee ID is required","Dialog", JOptionPane.ERROR_MESSAGE);
              }
              
              else if("".equals(Time_In.getText())){
              JOptionPane.showMessageDialog(new JFrame(), "Overtime Out is required","Dialog", JOptionPane.ERROR_MESSAGE);
              }
              
              else if("".equals (Date.getDate()==null)){
              JOptionPane.showMessageDialog(new JFrame(), "Date is required","Dialog", JOptionPane.ERROR_MESSAGE);
              }
             else {
                  
                   emid = SearchID.getText();
                   lt = Time_In.getText();
                   sd = Date.getDate();
                   
                   
                   
                   SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                   String sdStr = sdf.format(Date.getDate());
                   
                   
                  query = "INSERT INTO attendance (EmployeeID, OvertimeOut,Date)"
                          + "VALUES('"+emid+"','"+lt+"','"+sdStr+"')";
                  
                  st.executeUpdate(query);
                   SearchID.setText("");
                   Time_In.setText("");
                   Date.setDate(null);
                  
                   
                  
                   showMessageDialog(null,"Saved Data");
                       
                   con.close();
                  
              }
              
       }
    catch (Exception e){
              System.out.println("Error" + e.getMessage());
          }

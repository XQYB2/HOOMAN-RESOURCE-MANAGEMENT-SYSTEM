    private void Create1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Create1ActionPerformed
        // TODO add your handling code here:
        String EmployeeID;
        int notFound = 0;
        
          try {
              Class.forName("com.mysql.cj.jdbc.Driver");
              
              String url = "jdbc:MySQL://localhost:3308/hrms";
              String user = "root";
              String pass = "";
              
              Connection con = DriverManager.getConnection(url,user,pass);
              Statement st = con.createStatement();
              
              EmployeeID = SearchID.getText();
              if("".equals(EmployeeID)){
                  JOptionPane.showMessageDialog(new JFrame(), "ID is required","Dialog", JOptionPane.ERROR_MESSAGE);
              }
              else {
                  String sql = "SELECT * FROM  employeeinfo WHERE EmployeeID="+EmployeeID;
                  ResultSet rs =st.executeQuery(sql);
                  while (rs.next()){
                   EmployeeName.setText(rs.getString("EmployeeName"));
                   
                   
                   notFound = 1;
                   con.close();
                  }
                  if (notFound == 0){
                      JOptionPane.showMessageDialog(new JFrame(), "Invalid ID","Dialog", JOptionPane.ERROR_MESSAGE);
                  }
              }
              
              } catch (Exception e){
              System.out.println("Error" + e.getMessage());
              }
    }//GEN-LAST:event_Create1ActionPerformed


    private void Calculate2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Calculate2ActionPerformed
        // TODO add your handling code here:
        int lon,lat,abs,totale;
        double totald=0.0;
        String tax;
  
      
       try{ 
              if("".equals(Tax.getText())){
              JOptionPane.showMessageDialog(new JFrame(), "Tax is required","Dialog", JOptionPane.ERROR_MESSAGE);
              }
              else if("".equals(Loan.getText())){
              JOptionPane.showMessageDialog(new JFrame(), "Loan is required/Put 0 if none","Dialog", JOptionPane.ERROR_MESSAGE);
              }
              
              else if("".equals(Late.getText())){
              JOptionPane.showMessageDialog(new JFrame(), "Late Deductions is required/Put 0 if none","Dialog", JOptionPane.ERROR_MESSAGE);
              }
               else if("".equals(Absent.getText())){
              JOptionPane.showMessageDialog(new JFrame(), "Absent Deductions is required/Put 0 if none","Dialog", JOptionPane.ERROR_MESSAGE);
              }
             else {
                  tax = Tax.getText();
                  lon = Integer.parseInt(Loan.getText().trim().replace(",",""));
                  lat = Integer.parseInt(Late.getText().trim().replace(",",""));
                  abs= Integer.parseInt(Absent.getText().trim().replace(",",""));
                  totale= Integer.parseInt(Earn.getText().trim().replace(",",""));
                  
                  switch (tax){
                      case "15%": 
                          totald= (((totale*12)-250000)* 0.15)/12;break;
                      case "20%": 
                          totald = ((((totale*12)-400000)* 0.20)+22500)/12;break;
                      case "25%": 
                          totald = ((((totale*12)-800000)* 0.25)+102500)/12;break;
                  }
                  
                  totald+= lon + lat + abs;
                  
                  Deduct.setText(String.format("%.2f",totald));
                  
               }
            }catch (Exception e){
              System.out.println("Error" + e.getMessage());
          }
    }//GEN-LAST:event_Calculate2ActionPerformed


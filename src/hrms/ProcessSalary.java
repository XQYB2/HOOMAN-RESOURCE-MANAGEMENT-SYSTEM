    private void PrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PrintActionPerformed
        // TODO add your handling code here:
        try {
    // File path for saving the PDF
     String empID = EmployeeID.getText();
     String timestamp = new java.text.SimpleDateFormat("yyyy-MM-dd_HH-mm").format(new java.util.Date());
     String filePath = System.getProperty("user.home") + "/Downloads/HRMS_Salary_Report_ID#"+empID+"_"+ timestamp + ".pdf";

    // Create a new PDF document
    com.itextpdf.text.Document document = new com.itextpdf.text.Document();
    com.itextpdf.text.pdf.PdfWriter.getInstance(document, new java.io.FileOutputStream(filePath));
    document.open();

    // Fonts
    com.itextpdf.text.Font titleFont = new com.itextpdf.text.Font(com.itextpdf.text.Font.FontFamily.HELVETICA, 16, com.itextpdf.text.Font.BOLD);
    com.itextpdf.text.Font sectionFont = new com.itextpdf.text.Font(com.itextpdf.text.Font.FontFamily.HELVETICA, 13, com.itextpdf.text.Font.BOLD);
    com.itextpdf.text.Font normalFont = new com.itextpdf.text.Font(com.itextpdf.text.Font.FontFamily.HELVETICA, 12);

    // Title
    com.itextpdf.text.Paragraph title = new com.itextpdf.text.Paragraph("RAM-eSolutions\nEMPLOYEE SALARY REPORT", titleFont);
    title.setAlignment(com.itextpdf.text.Element.ALIGN_CENTER);
    document.add(title);
    document.add(new com.itextpdf.text.Paragraph("Generated on: " + new java.util.Date().toString(), normalFont));
    document.add(new com.itextpdf.text.Paragraph(" ")); // space

    // Employee Info Section
    document.add(new com.itextpdf.text.Paragraph("Employee Information", sectionFont));
    document.add(new com.itextpdf.text.Paragraph("Employee ID: " + EmployeeID.getText(), normalFont));
    document.add(new com.itextpdf.text.Paragraph("Employee Name: " + EmployeeName.getText(), normalFont));
    document.add(new com.itextpdf.text.Paragraph("Department: " + Department.getText(), normalFont));
    document.add(new com.itextpdf.text.Paragraph(" "));

    // Salary Info Section
    document.add(new com.itextpdf.text.Paragraph("Salary Information", sectionFont));
    document.add(new com.itextpdf.text.Paragraph(" "));
    document.add(new com.itextpdf.text.Paragraph("Earnings: ", sectionFont));
    document.add(new com.itextpdf.text.Paragraph("Base Salary: " + BaseSalary.getText(), normalFont));
    document.add(new com.itextpdf.text.Paragraph("Overtime: " + Overtime.getText(), normalFont));
    document.add(new com.itextpdf.text.Paragraph("Incentives: " + Incentives.getText(), normalFont));
    document.add(new com.itextpdf.text.Paragraph("Bonus: " + Bonus.getText(), normalFont));
    document.add(new com.itextpdf.text.Paragraph("Total Earnings: " + Earn.getText(), normalFont));
    document.add(new com.itextpdf.text.Paragraph(" "));
    document.add(new com.itextpdf.text.Paragraph("Deductions: ", sectionFont));
    document.add(new com.itextpdf.text.Paragraph("Tax: " + Tax.getText(), normalFont));
    document.add(new com.itextpdf.text.Paragraph("Loan: " + Loan.getText(), normalFont));
    document.add(new com.itextpdf.text.Paragraph("Late Deduction: " + Late.getText(), normalFont));
    document.add(new com.itextpdf.text.Paragraph("Absent Deduction: " + Absent.getText(), normalFont));
    document.add(new com.itextpdf.text.Paragraph("Total Deduction: " + Deduct.getText(), normalFont));
    document.add(new com.itextpdf.text.Paragraph(" "));
    document.add(new com.itextpdf.text.Paragraph("Total Salary: " + Total.getText(), sectionFont));

    // Close PDF
    document.close();

    javax.swing.JOptionPane.showMessageDialog(this,
        "Employee report exported successfully!\nSaved at: " + filePath,
        "Export Successful", javax.swing.JOptionPane.INFORMATION_MESSAGE);

} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Error exporting PDF: " + e.getMessage(),
        "Export Failed", javax.swing.JOptionPane.ERROR_MESSAGE);
}

    }//GEN-LAST:event_PrintActionPerformed


    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        String SalaryID;
        int notFound = 0;
        
          try {
              Class.forName("com.mysql.cj.jdbc.Driver");
              
              String url = "jdbc:MySQL://localhost:3308/hrms";
              String user = "root";
              String pass = "";
              
              Connection con = DriverManager.getConnection(url,user,pass);
              Statement st = con.createStatement();
              
              SalaryID = SearchID.getText();
              if("".equals(SalaryID)){
                  JOptionPane.showMessageDialog(new JFrame(), "ID is required","Dialog", JOptionPane.ERROR_MESSAGE);
              }
              else {
                   String sql = "SELECT p.BaseSalary, p.EmployeeID, e.EmployeeName, e.Department " +
                 "FROM payroll p " +
                 "INNER JOIN employeeinfo e ON p.EmployeeID = e.EmployeeID " +
                 "WHERE p.SalaryID = " + SalaryID;

                  ResultSet rs = st.executeQuery(sql);
    

                  while (rs.next()) {
                   EmployeeID.setText(rs.getString("EmployeeID"));
                   BaseSalary.setText(rs.getString("BaseSalary"));
                   EmployeeName.setText(rs.getString("EmployeeName"));
                   Department.setText(rs.getString("Department"));
     
                   
                   
                   
                   notFound = 1;
                   con.close();
                  }
                  if (notFound == 0){
                      JOptionPane.showMessageDialog(new JFrame(), "Invalid ID","Dialog", JOptionPane.ERROR_MESSAGE);
                  }
              }
              
              } catch (Exception e){
              System.out.println("Error" + e.getMessage());
              }
    }//GEN-LAST:event_SearchActionPerformed

